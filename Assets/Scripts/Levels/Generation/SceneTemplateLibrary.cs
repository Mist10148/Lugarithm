    /// <summary>World size of a template sprite (streaming/AABB math).</summary>
    public static Vector2 SizeOf(string sprite)
    {
        foreach (Template t in Templates)
            if (t.sprite == sprite) return t.size;
        return Vector2.zero;
    }

