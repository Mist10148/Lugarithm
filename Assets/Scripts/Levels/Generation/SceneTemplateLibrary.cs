using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// One whole-scene background chunk placed in the world: which template sprite,
/// where its center sits, and its chain order.
/// </summary>
public class ScenePlacement
{
    public string  sprite;
    public Vector2 center;
    public int     order;
}

/// <summary>A branch side-street painted into a template: root on the trunk, tip at the stop.</summary>
public struct SceneArm
{
    public Vector2 root;
    public Vector2 tip;
}

/// <summary>Geometry for one planned chunk of the whole-scene route.</summary>
public class SceneChunkPlan
{
    public ScenePlacement placement;
    /// <summary>Graph vertices after the entry point, in travel order (cardinal
    /// edges only — Manhattan for Automation); the last one is the chunk's exit.</summary>
    public List<Vector2> vertices = new List<Vector2>();
    /// <summary>Parallel to <see cref="vertices"/>: true where the vertex is a 90 degree turn.</summary>
    public List<bool> corners = new List<bool>();
    /// <summary>The smooth driving path entry→exit (exclusive of the entry point,
    /// inclusive of the exit). Follows the painted curve where the template has
    /// one; used for the Manual drive/traffic polyline, NOT for graph nodes.</summary>
    public List<Vector2> drivePath = new List<Vector2>();
    /// <summary>Extra road segments covering the painted curve so off-road math
    /// hugs the art. Never become graph edges (may be diagonal).</summary>
    public List<RoadSegment> extraSegments = new List<RoadSegment>();
    public List<SceneArm> arms = new List<SceneArm>();
    public Vector2 exit;
    /// <summary>Travel direction leaving this chunk.</summary>
    public Vector2 exitHeading;
}

/// <summary>
/// Whole-scene driving background planner. Every template's road ports are
/// generated from the source art by art_src/driving/build_background.py. Each
/// template stores measured road ports, so the painted roads line up at seams
