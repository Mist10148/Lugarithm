    static Vector2 Cardinal(Vector2 v)
    {
        return Mathf.Abs(v.x) >= Mathf.Abs(v.y)
            ? new Vector2(Mathf.Sign(v.x), 0f)
            : new Vector2(0f, Mathf.Sign(v.y));
    }

    static Vector2 Perp(Vector2 v) => new Vector2(-v.y, v.x);

    static float PolylineLength(Vector2[] pts)
    {
        float len = 0f;
        for (int i = 1; i < pts.Length; i++) len += Vector2.Distance(pts[i - 1], pts[i]);
        return len;
    }

    static float ArcAlong(Vector2[] pts, Vector2 p)
    {
        float best = float.MaxValue, bestArc = 0f, arc = 0f;
        for (int i = 1; i < pts.Length; i++)
        {
            Vector2 a = pts[i - 1], b = pts[i];
            Vector2 ab = b - a;
            float t = ab.sqrMagnitude < 1e-4f ? 0f
                : Mathf.Clamp01(Vector2.Dot(p - a, ab) / ab.sqrMagnitude);
            Vector2 c = a + ab * t;
            float d = (c - p).sqrMagnitude;
            if (d < best) { best = d; bestArc = arc + ab.magnitude * t; }
            arc += ab.magnitude;
        }
        return bestArc;
    }

    static int NearestVertex(Vector2[] pts, float targetArc, HashSet<int> used)
    {
        float arc = 0f, bestDiff = float.MaxValue;
        int best = 0;
        for (int i = 0; i < pts.Length; i++)
        {
            if (i > 0) arc += Vector2.Distance(pts[i - 1], pts[i]);
            if (used.Contains(i)) continue;
            float diff = Mathf.Abs(arc - targetArc);
            if (diff < bestDiff) { bestDiff = diff; best = i; }
        }
        return best;
    }

    static int Hash(int seed, int salt)
    {
        unchecked
        {
            int h = (int)2166136261;
            h = (h ^ seed) * 16777619;
            h = (h ^ salt) * 16777619;
            return h & 0x7fffffff;
        }
    }
