/// </summary>
public static class SceneTemplateLibrary
{
    class Template
    {
        public string sprite;
        public Vector2 size;
        public Vector2 northPort = Invalid;
        public Vector2 southPort = Invalid;
        public Vector2 eastPort  = Invalid;
        public Vector2 westPort  = Invalid;
        /// <summary>Largest corner-arc radius (world units) between this
        /// template's perpendicular roads that stays fully on the painted
        /// asphalt — measured against the source pixels. 0 for straights.</summary>
        public float fillet;
        /// <summary>
        /// Explicit drive transitions measured/approved for this art. Each
        /// code is entry edge then exit edge, e.g. "SN" means entering from
        /// the south edge and exiting the north edge.
        /// </summary>
        public string[] allow;
        public Transition[] transitions;
    }

    class Transition
    {
        public string code;
        public Vector2 graphCorner;
        /// <summary>Template-local centerline, including both edge ports.</summary>
        public Vector2[] path;
    }

