    /// <summary>
    /// Plans the next chunk continuing from <paramref name="entry"/> traveling
    /// <paramref name="headingIn"/>. Deterministic per seed. May turn 90° along
    /// a painted curve — never against <paramref name="forwardAxis"/>, and it
    /// steers back after a lateral leg. Avoids repeating any sprite used in the
    /// last few chunks and avoids overlapping already placed chunks.
    /// </summary>
    public static SceneChunkPlan PlanChunk(Vector2 entry, Vector2 headingIn, Vector2 forwardAxis,
                                           IList<ScenePlacement> existing, bool forceStraight,
                                           int seed, int order)
    {
        Vector2 dIn = Cardinal(headingIn);
        Vector2 fwd = Cardinal(forwardAxis.sqrMagnitude > 0.01f ? forwardAxis : Vector2.up);

        var exitDirs = new List<Vector2>();
        bool lateral = Mathf.Abs(Vector2.Dot(dIn, fwd)) < 0.5f;
        int roll = Hash(seed, 5) % 100;
        if (forceStraight) exitDirs.Add(dIn);
        else if (lateral)
        {
            if (roll < 80) { exitDirs.Add(fwd); exitDirs.Add(dIn); }
            else           { exitDirs.Add(dIn); exitDirs.Add(fwd); }
        }
        else
        {
            // Turn-heavy town core, opening into longer straight runs the
            // further the player drives: the turn chance decays with the
            // chunk order (~65% on the first chunks → ~25% past chunk 14).
            Vector2 side = (Hash(seed, 7) % 2 == 0) ? Perp(dIn) : -Perp(dIn);
            int turnPct = Mathf.RoundToInt(Mathf.Lerp(65f, 25f, Mathf.Clamp01(order / 14f)));
            if (roll >= turnPct) { exitDirs.Add(dIn); exitDirs.Add(side); }
            else                 { exitDirs.Add(side); exitDirs.Add(dIn); }
        }
        exitDirs.Add(dIn);   // safety net

        var recent = new HashSet<string>();
        if (existing != null)
            for (int i = Mathf.Max(0, existing.Count - NoRepeatWindow); i < existing.Count; i++)
                recent.Add(existing[i].sprite);

        SceneChunkPlan firstValid = null;
        foreach (Vector2 dOut in exitDirs)
        {
            if (Vector2.Dot(dOut, fwd) < -0.5f) continue;

            var candidates = new List<Template>();
            foreach (Template t in Templates)
                if (HasTransition(t, -dIn, dOut)) candidates.Add(t);
            if (candidates.Count == 0) continue;

            // prefer non-recent sprites; when the whole pool is recent, at
            // least never place the same sprite twice in a row
            var fresh = candidates.FindAll(t => !recent.Contains(t.sprite));
            var pool = fresh.Count > 0 ? fresh : candidates;
            if (fresh.Count == 0 && existing != null && existing.Count > 0 && candidates.Count > 1)
            {
                string last = existing[existing.Count - 1].sprite;
                var notLast = candidates.FindAll(t => t.sprite != last);
                if (notLast.Count > 0) pool = notLast;
            }

            int pick = Hash(seed, 11) % pool.Count;
            for (int k = 0; k < pool.Count; k++)
            {
                Template t = pool[(pick + k) % pool.Count];
                SceneChunkPlan plan = Compose(t, entry, dIn, dOut, seed, order);
                if (firstValid == null) firstValid = plan;
                if (!Collides(plan.placement, existing)) return plan;
            }
        }
        return firstValid;   // boxed in: accept the overlap (dither hides it)
    }

    /// <summary>
    /// Plans the initial town from <paramref name="start"/> along
    /// <paramref name="heading"/> covering <paramref name="targetLength"/>:
    /// a straight opening chunk, then turn-heavy streets (the same order-decay
    /// cadence as streaming, so the town core winds and later roads run long).
    /// Every choice depends only on the chunk index — NEVER the seed — so a
    /// level's starting town (and its story anchors) is identical across seeds.
    /// </summary>
    public static List<SceneChunkPlan> PlanInitial(Vector2 start, Vector2 heading,
                                                   float targetLength, int seed)
    {
        var plans = new List<SceneChunkPlan>();
        var placed = new List<ScenePlacement>();
        Vector2 pos = start;
        Vector2 fwd = Cardinal(heading);
        Vector2 dir = fwd;
        float covered = 0f;
        int order = 0;
        while (covered < targetLength || plans.Count == 0)
        {
            SceneChunkPlan p = PlanChunk(pos, dir, fwd, placed, forceStraight: order == 0,
                                         Hash(1237, order), order);
            if (p == null) break;
            plans.Add(p);
            placed.Add(p.placement);
            // progress counts along the level's forward axis, so lateral legs
            // wind the town without shortening it
            covered += Mathf.Max(0f, Vector2.Dot(p.exit - pos, fwd));
            pos = p.exit;
            dir = p.exitHeading;
            order++;
            if (order > 16) break;
        }
        return plans;
    }

