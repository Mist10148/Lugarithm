    static bool HasPort(Template t, Vector2 side)
    {
        return !float.IsNaN(Port(t, side).x);
    }

    static bool HasTransition(Template t, Vector2 entrySide, Vector2 exitSide)
    {
        if (!HasPort(t, entrySide) || !HasPort(t, exitSide)) return false;
        if (t.allow == null || t.allow.Length == 0) return true;

        string code = SideCode(entrySide) + SideCode(exitSide);
        for (int i = 0; i < t.allow.Length; i++)
            if (t.allow[i] == code) return true;
        return false;
    }

    static Transition FindTransition(Template t, Vector2 entrySide, Vector2 exitSide)
    {
        string code = SideCode(entrySide) + SideCode(exitSide);
        if (t.transitions == null) return null;
        for (int i = 0; i < t.transitions.Length; i++)
            if (t.transitions[i].code == code) return t.transitions[i];
        return null;
    }

    static string SideCode(Vector2 side)
    {
        if (side.y > 0.5f) return "N";
        if (side.y < -0.5f) return "S";
        if (side.x > 0.5f) return "E";
        return "W";
    }

    static Vector2 Port(Template t, Vector2 side)
    {
        if (side.y > 0.5f)  return t.northPort;
        if (side.y < -0.5f) return t.southPort;
        if (side.x > 0.5f)  return t.eastPort;
        return t.westPort;
    }

    static SceneChunkPlan Compose(Template t, Vector2 entry, Vector2 dIn, Vector2 dOut,
                                  int seed, int order)
    {
        Vector2 pIn = Port(t, -dIn);
        Vector2 pOut = Port(t, dOut);
        Vector2 center = entry - pIn - dIn * (Overlap * 0.5f);
        Vector2 exit = center + pOut - dOut * (Overlap * 0.5f);

        var plan = new SceneChunkPlan
        {
            placement = new ScenePlacement { sprite = t.sprite, center = center, order = order },
            exit = exit,
            exitHeading = dOut,
        };

        bool turning = Mathf.Abs(Vector2.Dot(dIn, dOut)) < 0.5f;
        if (turning)
        {
            Transition transition = FindTransition(t, -dIn, dOut);
            Vector2 nsPort = Mathf.Abs(dIn.y) > 0.5f ? pIn : pOut;
            Vector2 ewPort = Mathf.Abs(dIn.y) > 0.5f ? pOut : pIn;
            Vector2 corner = center + (transition != null
                ? transition.graphCorner
                : new Vector2(nsPort.x, ewPort.y));
            plan.vertices.Add(corner);
            plan.corners.Add(true);

            if (transition != null && transition.path != null && transition.path.Length > 1)
            {
                Vector2 prev = entry;
                // First sample is the template port, represented by entry after
                // seam overlap. Consume the remaining explicit audited samples.
                for (int i = 1; i < transition.path.Length - 1; i++)
                {
                    Vector2 p = center + transition.path[i];
                    plan.drivePath.Add(p);
                    plan.extraSegments.Add(new RoadSegment(prev, p, true));
                    prev = p;
                }
                plan.extraSegments.Add(new RoadSegment(prev, exit, true));
            }
            else
            {
                plan.drivePath.Add(corner);
            }
        }
        else
        {
            // Straight pass: 1-2 boardable interior stops, clear of the ports.
            float len = Vector2.Distance(entry, exit);
            Vector2 dirN = (exit - entry) / Mathf.Max(len, 0.01f);
            if (len > 22f)
            {
                // Stops sit in the 0.28-0.36 / 0.64-0.72 windows — never near
                // mid-chunk, where the T/4-way painted into most scenes
                // crosses (a stop sign in the middle of a junction reads broken).
                int count = len > 44f ? 1 + Hash(seed, 41) % 2 : 1;
                for (int i = 0; i < count; i++)
                {
                    float f;
                    if (count == 1)
                        f = (Hash(seed, 47) % 2 == 0 ? 0.28f : 0.64f)
                            + (Hash(seed, 43) % 5) * 0.02f;
                    else
                        f = i == 0 ? 0.30f : 0.70f;
                    Vector2 v = entry + dirN * Mathf.Clamp(len * f, 9f, len - 9f);
                    plan.vertices.Add(v);
                    plan.corners.Add(false);
                    plan.drivePath.Add(v);
                }
            }
        }

        plan.vertices.Add(exit);
        plan.corners.Add(false);
        plan.drivePath.Add(exit);
        return plan;
    }

    static bool Collides(ScenePlacement p, IList<ScenePlacement> existing)
    {
        if (existing == null) return false;
        Vector2 size = SizeOf(p.sprite);
        var r = new Rect(p.center - size * 0.5f + Vector2.one * 2f, size - Vector2.one * 4f);
        foreach (ScenePlacement e in existing)
        {
            Vector2 es = SizeOf(e.sprite);
            var er = new Rect(e.center - es * 0.5f + Vector2.one * 2f, es - Vector2.one * 4f);
            if (r.Overlaps(er)) return true;
        }
        return false;
    }

    /// <summary>
    /// Rebinds authored story anchors onto the planned trunk: each anchor keeps
    /// its arc fraction along the route but moves to the nearest planned vertex,
    /// preserving name, kind and story order. With the seed-stable initial chain
    /// the resulting positions are identical for every seed.
    /// </summary>
    public static AnchorNode[] RemapAnchors(AnchorNode[] anchors, Vector2[] authoredTrunk,
                                            Vector2[] plannedTrunk)
    {
        if (anchors == null || anchors.Length == 0 || plannedTrunk == null || plannedTrunk.Length < 2)
            return anchors;

        float authoredLen = PolylineLength(authoredTrunk);
        float plannedLen = PolylineLength(plannedTrunk);
        var used = new HashSet<int>();
        var result = new AnchorNode[anchors.Length];

        for (int i = 0; i < anchors.Length; i++)
        {
            AnchorNode a = anchors[i];
            float frac;
            if (a.kind == AnchorKind.TerminalStart) frac = 0f;
            else if (a.kind == AnchorKind.TerminalEnd) frac = 1f;
            else if (authoredLen > 0.01f) frac = Mathf.Clamp01(ArcAlong(authoredTrunk, a.position) / authoredLen);
            else frac = 0.5f;

            int best = NearestVertex(plannedTrunk, frac * plannedLen, used);
            used.Add(best);
            result[i] = new AnchorNode { name = a.name, kind = a.kind, position = plannedTrunk[best] };
        }
        return result;
    }

